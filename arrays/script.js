// CRIAR ARRAY
let nomes
nomes=["Bruna","Felipe","Fernando","Jessica"]

// push -> adiciona 
nomes.push("Luciene")

// unshift -> adiciona ao final do array
nomes.unshift("Francisco")

//pop -> remove o último item do array
nomes.pop()

//shift -> remove o primeiro item do array 
nomes.shift()

//length -> informa o tamanho do array ( quantidade de itens)
console.log(nomes.length)

//indexOf -> retorna o índice de um item do array
console.log(nomes.indexOf("Jessica"))